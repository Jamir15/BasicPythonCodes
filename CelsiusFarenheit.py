#celsius to farenheit

print('This code will convert celsius to farenheit') #Display message to user

#I'm using float here to allow decimal points
celsius = float(input('Enter temperature in celsius: ')) #get user input

#formula to convert celsius to farenheit, kinuha ko lang sa Google hahaha!
farenheit = (celsius * 9/5) + 32

print(celsius, 'is ', farenheit, 'in farenheit') #print result